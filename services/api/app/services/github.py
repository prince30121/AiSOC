async def _get_default_branch_and_sha(
    client: httpx.AsyncClient,
    headers: dict[str, str],
    repo: str,
) -> tuple[str, str | None]:
    try:
        resp = await client.get(f"/repos/{repo}", headers=headers)
        resp.raise_for_status()
        data: dict[str, Any] = resp.json()
        default_branch: str = data.get("default_branch", "main")

        ref_resp = await client.get(f"/repos/{repo}/git/ref/heads/{default_branch}", headers=headers)
        ref_resp.raise_for_status()
        sha: str = ref_resp.json()["object"]["sha"]
        return default_branch, sha
    except Exception as exc:
        logger.warning("GitHub: failed to resolve default branch SHA — %s", exc)
        return "main", None


async def _create_branch(
    client: httpx.AsyncClient,
    headers: dict[str, str],
    repo: str,
    branch_name: str,
    sha: str,
) -> bool:
    try:
        resp = await client.post(
            f"/repos/{repo}/git/refs",
            headers=headers,
            json={"ref": f"refs/heads/{branch_name}", "sha": sha},
        )
        if resp.status_code == 422:
            # Branch already exists — safe to continue.
            return True
        resp.raise_for_status()
        return True
    except Exception as exc:
        logger.warning(
            "GitHub: failed to create branch %s — %s",
            _safe_for_log(branch_name),
            type(exc).__name__,
        )
        return False


async def _commit_file(
    client: httpx.AsyncClient,
    headers: dict[str, str],
    repo: str,
    branch_name: str,
    file_path: str,
    content: str,
    message: str,
) -> bool:
    encoded = base64.b64encode(content.encode()).decode()
    payload: dict[str, Any] = {
        "message": message,
        "content": encoded,
        "branch": branch_name,
    }

    # Fetch existing file SHA if it exists (so GitHub accepts an update).
    try:
        existing = await client.get(
            f"/repos/{repo}/contents/{file_path}",
            headers=headers,
            params={"ref": branch_name},
        )
        if existing.status_code == 200:
            payload["sha"] = existing.json().get("sha")
    except Exception as exc:  # noqa: BLE001 — SHA fetch is best-effort; missing SHA just overwrites
        logger.debug(
            "GitHub: could not fetch existing SHA for %s: %s",
            _safe_for_log(file_path),
            type(exc).__name__,
        )

    try:
        resp = await client.put(
            f"/repos/{repo}/contents/{file_path}",
            headers=headers,
            json=payload,
        )
        resp.raise_for_status()
        return True
    except Exception as exc:
        logger.warning(
            "GitHub: failed to commit file %s — %s",
            _safe_for_log(file_path),
            type(exc).__name__,
        )
        return False


async def _open_pr(
    client: httpx.AsyncClient,
    headers: dict[str, str],
    repo: str,
    title: str,
    body: str,
    head: str,
    base: str,
) -> str | None:
    try:
        resp = await client.post(
            f"/repos/{repo}/pulls",
            headers=headers,
            json={
                "title": title,
                "body": body,
                "head": head,
                "base": base,
                "draft": True,
            },
        )
        if resp.status_code == 422:
            # PR already exists for this branch — try to find it.
            search = await client.get(
                f"/repos/{repo}/pulls",
                headers=headers,
                params={"head": f"{repo.split('/')[0]}:{head}", "state": "open"},
            )
            if search.status_code == 200 and search.json():
                return search.json()[0]["html_url"]
            return None
        resp.raise_for_status()
        return resp.json()["html_url"]
    except Exception as exc:
        logger.warning(
            "GitHub: failed to open PR for branch %s — %s",
            _safe_for_log(head),
            type(exc).__name__,
        )
        return None
